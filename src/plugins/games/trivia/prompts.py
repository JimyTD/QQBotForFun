HOST_SYSTEM = """你是一个出"线索猜答案"小游戏的出题者。玩家会看到 5 条线索（从隐晦到直白），要猜出答案。

{type_guide}

【通用输出要求】
- answer：**必须是该类型下最标准、最广为人知的主名称**（不是绰号、昵称或诗意称呼）
  - ✅ 正确："挪威"、"阿根廷"、"故宫"、"长城"、"孙悟空"、"北京烤鸭"
  - ❌ 错误："冰原之国"（挪威的绰号，应放 aliases）、"南美足球王国"（应写"巴西"）
  - 准则：answer 应该是玩家**最可能直接脱口说出的那个名字**
- answer：3-15 字
- aliases：2-5 个常见别名 / 英文名 / 简称 / 诗意称呼，**不得与 answer 完全相同**
- clues：**恰好 5 条**，按"从难到易"排列，每条一句话（15-50 字）
- explanation：一句话讲解（20-60 字），答对或跳过后展示，给玩家一点小知识收获

⚠️ 最重要的一条：所有线索里**绝对不能出现答案本身的字**，也不能出现答案的明显别称。
    比如答案"加拿大"，线索里不能出现"加拿大"、"Canada"、"枫叶国"等字样（这些只能放 aliases）。

⚠️ 线索**必须基于真实事实**，不得编造不存在的成就、地点、人物、历史事件。
    例如不能说"全球最长跨海大桥连接南北岛"（新西兰没这桥）这种凭空捏造。

⚠️ 线索风格要"有信息量"，不要说废话（比如"它是个很棒的 X"这种等于没说）。

只输出 JSON，无多余文字：
{{
  "answer": "<该事物最标准的主名称>",
  "aliases": ["<别名1>", "<别名2>"],
  "clues": [
    "<最隐晦的线索>",
    "<第二条>",
    "<第三条>",
    "<第四条>",
    "<最直白的送分线索>"
  ],
  "explanation": "<一句话科普>"
}}
"""


HOST_USER = "请出一道新的{type_name}题。答案要新鲜，避免和最近常见的题重复。"


def build_host_system_prompt(type_id: str) -> str:
    """根据 type_id 渲染出题 system prompt。"""
    if type_id not in TYPE_STYLE_GUIDES:
        raise ValueError(f"unknown trivia type: {type_id}")
    return HOST_SYSTEM.format(type_guide=TYPE_STYLE_GUIDES[type_id]["guide"])


def build_host_user_prompt(type_id: str, avoid: list[str] | None = None) -> str:
    """渲染出题 user prompt。

    avoid: 本局已出过的答案/别名列表。会被展开成显式禁词，**强制** LLM 换新答案。
    """
    if type_id not in TYPE_STYLE_GUIDES:
        raise ValueError(f"unknown trivia type: {type_id}")
    base = HOST_USER.format(type_name=TYPE_STYLE_GUIDES[type_id]["name"])
    if avoid:
        # 去重、去空、截断到前 30 条（避免 prompt 过长）
        seen: set[str] = set()
        deduped: list[str] = []
        for item in avoid:
            if not isinstance(item, str):
                continue
            s = item.strip()
            if not s or s in seen:
                continue
            seen.add(s)
            deduped.append(s)
        deduped = deduped[:30]
        if deduped:
            listing = "、".join(f"「{x}」" for x in deduped)
            base += (
                f"\n\n⚠️ 本局已经出过以下答案及其别名，**严禁**再次作为答案或别名出现，"
                f"也不要出内容高度相近的题：{listing}。"
                f"\n请选择一个明显不同的新答案。"
            )
    return base


def type_display_name(type_id: str) -> str:
    info = TYPE_STYLE_GUIDES.get(type_id)
    if info is None:
        return type_id
    return f"{info['emoji']} {info['name']}"


