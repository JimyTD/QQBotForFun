def build_alt_clue_system_prompt(type_id: str) -> str:
    """第 2 套线索的 system prompt。复用同类型的风格卡。"""
    if type_id not in TYPE_STYLE_GUIDES:
        raise ValueError(f"unknown trivia type: {type_id}")
    return ALT_CLUE_SYSTEM.format(type_guide=TYPE_STYLE_GUIDES[type_id]["guide"])


def build_alt_clue_user_prompt(
    type_id: str,
    *,
    answer: str,
    aliases: list[str],
    existing_clues: list[str],
) -> str:
    """第 2 套线索的 user prompt：显式告诉 LLM 答案、别名、已有线索。"""
    if type_id not in TYPE_STYLE_GUIDES:
        raise ValueError(f"unknown trivia type: {type_id}")
    aliases_s = "、".join(f"「{a}」" for a in aliases) if aliases else "（无）"
    existing_s = "\n".join(f"  {i + 1}. {c}" for i, c in enumerate(existing_clues))
    return (
        f"请为以下答案生成第二套线索：\n\n"
        f"答案：「{answer}」\n"
        f"别名：{aliases_s}\n\n"
        f"已有第一套线索（务必避开重复，角度要明显不同）：\n{existing_s}\n\n"
        f"请直接输出 JSON。"
    )
